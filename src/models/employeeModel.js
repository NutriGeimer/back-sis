class EmployeeModel {
    constructor(id, fullname, email, phone, address, rfc, salary) {
        this.id = id;
        this.fullname = fullname;
        this.email = email;
        this.phone = phone;
        this.address = address;
        this.rfc = rfc;
        this.salary = salary;
    }
}

export default EmployeeModel;
